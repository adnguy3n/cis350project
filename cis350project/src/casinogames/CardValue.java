package casinogames;
/**
 * enum containing the possible values (ace through king) for a card.
 */
public enum CardValue {
	/**
	 * The possible enum values for the card's value.
	 * Ace through seven
	 */
	ACE, TWO, THREE, FOUR, FIVE, SIX, SEVEN, 
	
	/**
	 * The possible enum values for the card's value.
	 * Eight through King.
	 */
	EIGHT, NINE, TEN, JACK, QUEEN, KING
}
